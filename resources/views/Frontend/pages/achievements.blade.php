            .. يؤدى  ذلك إلى حدوث تلوث مياه الصرف الزراعي وما يترتب عليه من ضرر على البيئة والصحة العامة خصوصاً وأن مياه الصرف الزراعي تعود إلى النيل في مصر العليا ويعاد استخدامها بالرفع في الوجه البحري والفيوم .
            .. تتولى الهيئة مسئوليه تنفيذ قانون حماية المجارى المائية من التلوث فيما يخص شبكة الصرف الزراعي وقد حدد القانون رقم 48 لسنة 1982 ولائحته التنفيذية معايير صرف المخلفات السائلة والمعالجة إلى مسطحات المياه العذبة في مواده 60 إلى 65 وإلى مسطحات المياه الغير عذبة في مواده 66 إلى 69 ويعتبر القانون من أهم القوانين التي لها علاقة صريحة بالمحافظة على نوعية المياه بتحرير محاضر المخالفات واستصدار قرارات الإزالة والتنسيق مع الأجهزة الأمنية لتنفيذ القانون وإزالة التعديات .
            .. تقوم الهيئة بدراسة طلبات الترخيص بصرف السيب النهائي لمحطات معالجة الصرف الصحي على شبكات المصارف العامة والتأثير المتوقع على نوعية المياه بهذه المصارف وخاصة تلك التي من المقرر إعادة استخدام مياهها ويتم المشاركة باللجان الفرعية بالمحافظات لدراسة هذه الطلبات ورفع التوصيات للعرض على اللجنة الدائمة المشتركة لمشروعات مياه الشرب والصرف الصحي لإجراء المزيد من الدراسات الفنية والتوقعات المستقبلية لتأثير السيب النهائي لهذه المحطات واتخاذ القرارات اللازمة بشأنها لإدخالها حيز التنفيذ .
