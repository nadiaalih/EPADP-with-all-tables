    الادارة العامة للبيئة
    تعمل الادارة العامة للبيئة بالهيئة لتحقيق أهداف وغايات خطة الإدارة البيئية والاجتماعية بالمشروع القومي الثالث للصرف والتي ستضمن أن أعمال التنفيذ  والتشغيل للمشروع يتضمن جميع تدابير التخفيف المقترحة والامتثال للمبادئ التوجيهية الدولية واللوائح المصرية التي يجب اتباعها أثناء تنفيذ خطة الإدارة البيئية والاجتماعية هي:
    •	معايير بنك التنمية الالماني  / KFW و الاتحاد الاوربي والتنفيذ المستدام
    •	معاييرالبنك الافريقي
    •	معايير البنك الدولي  WB / مؤسسة التمويل الدولية  IFC
    •	القوانين والمعايير البيئية المصرية.
    •	خطط العمل الوطنية (NAPs) واستراتيجية التنمية الزراعية المستدامة 2030 (SADS) والوطنية
    •	المساهمات المحددة (NDCs) للعمل المناخي
    و يعتبر البرنامج القومي الثالث للصرف له  تأثير بيئي إيجابي من خلال مساهمتها في تحسين الأراضي وإدارة المياه. كما يهدف إلى دعم التنمية الاجتماعية والاقتصادية في مصر من خلال المشروع لتحقيق الأهداف التاليه:
    1.	زيادة الغلة الزراعية في مناطق المشروع من خلال تحسين كفاءة الصرف
    2.	خفض منسوب المياه الجوفية بشكل مستدام لتجنب التشبع بالمياه وتملح الأراضي الزراعية .
    3.	تطبيق معايير الاستثمار  مع تبني مبادئ نهج القطاع المتكامل المشترك
    4.	تعزيز قدرات التخطيط الخاصة بالهيئة المصرية العامة لتحسين إدارة اليابس والماء في مصر
